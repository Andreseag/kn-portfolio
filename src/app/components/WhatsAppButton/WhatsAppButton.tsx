"use client";

import React from "react";
import { motion } from "motion/react";

interface WhatsAppButtonProps {
  phoneNumber: string; // Ejemplo: "5491122334455" (sin el +)
  message?: string; // Mensaje predeterminado opcional
}

const WhatsAppButton = ({
  phoneNumber,
  message = "Hola! Me gustaría agendar una sesión.",
}: WhatsAppButtonProps) => {
  const handleClick = () => {
    // Codificamos el mensaje para que sea válido en una URL
    const encodedMessage = encodeURIComponent(message);
    const whatsappUrl = `https://wa.me/${phoneNumber}?text=${encodedMessage}`;

    // Abrimos en una nueva pestaña
    window.open(whatsappUrl, "_blank", "noopener,noreferrer");
  };

  return (
    <motion.button
      onClick={handleClick}
      initial={{ opacity: 0, scale: 0.5, y: 20 }}
      animate={{ opacity: 1, scale: 1, y: 0 }}
      whileHover={{ scale: 1.1, backgroundColor: "#25D366" }} // Cambia a verde WhatsApp al pasar el mouse
      whileTap={{ scale: 0.9 }}
      className="fixed bottom-8 right-8 z-999 cursor-pointer bg-black text-white p-4 rounded-full shadow-2xl flex items-center justify-center transition-colors duration-300"
      aria-label="Contactar por WhatsApp">
      {/* SVG OFICIAL DE WHATSAPP */}
      <svg
        xmlns="http://www.w3.org/2000/svg"
        width="32"
        height="32"
        fill="currentColor" /* Esto hace que el icono sea blanco (por la clase text-white) */
        viewBox="0 0 24 24">
        <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.414 0 .004 5.412.001 12.049a11.815 11.815 0 001.592 5.97L0 24l6.117-1.605a11.794 11.794 0 005.926 1.586h.005c6.636 0 12.046-5.411 12.049-12.049a11.825 11.825 0 00-3.528-8.513z" />
      </svg>
    </motion.button>
  );
};

export default WhatsAppButton;
